package com.julian.superliga.vo;

public class RankingMes {

	private int mes;
	private int anio;

	public RankingMes() {
	}

	public RankingMes(int mes, int anio) {
		super();
		this.mes = mes;
		this.anio = anio;
	}

	public int getMes() {
		return mes;
	}

	public void setMes(int mes) {
		this.mes = mes;
	}

	public int getAnio() {
		return anio;
	}

	public void setAnio(int anio) {
		this.anio = anio;
	}
}
